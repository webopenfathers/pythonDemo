# 二：字典进阶
# 1、基础语法
# a.setdefault('a',[])
# 2、语法优势
# 存在则取值,不存在则插入值,并返回值

i={}

j=i.get('a',False)
print(j) # False
print(i) # {}

# 存在则取值,不存在则插入值,并返回值
k=i.setdefault('a',False)
print(k) # False
print(i) # {'a': False}

l=i.setdefault('e',[1,2,3])
print(l) # [1, 2, 3]
print(i) # {'a': False, 'e': [1, 2, 3]}

print(id(l)) # 2011564350976
print(id(i['e'])) # 2011564350976

l.append('456')
print(l) # [1, 2, 3, '456']
print(i) # {'a': False, 'e': [1, 2, 3, '456']}

