# 1、基础语法
b={ str(i):[i,i*i] for i in range(1,5)}
print(b) # {'1': [1, 1], '2': [2, 4], '3': [3, 9], '4': [4, 16]}

# 2、判断条件
# 2-1、{参数1 : 参数2 for 参数 in 可迭代对象 if 条件}
c={ str(n):[n,n*2] for n in range(6,11) if n!=7 }
print(c) # {'6': [6, 12], '8': [8, 16], '9': [9, 18], '10': [10, 20]}

# 2-2、{参数1 : 参数2 if条件 else 值 for 参数 in 可迭代对象}
d={str(i):i if i!=2 else '8' for i in range(1,6) }
print(d) # {'1': 1, '2': '8', '3': 3, '4': 4, '5': 5}

# 3、内置的zip
# zip() 函数通常用于将多个可迭代对象打包成一个元组序列
e=list(zip([1,2],[3,4]))
print(e) # [(1, 3), (2, 4)]

f=list(zip([1,2],[3,4],[5,6]))
print(f) # [(1, 3, 5), (2, 4, 6)]

g=dict(zip([1,2],[3,4]))
print(g) # {1: 3, 2: 4}

# ************报错
# 第一个元素：(1, 3, 5) - 长度为 3
# 第二个元素：(2, 4, 6) - 长度为 3
